// 根据卦象的基础爻型（六爻配置）获取卦象
export const getHexagramByLines = (lines: string[]): HexagramData | undefined => {
  if (!Array.isArray(lines) || lines.length !== 6) {
    return undefined;
  }
  
  return hexagramsData.find(hexagram => 
    hexagram.lines.base.every((lineType, index) => lineType === lines[index])
  );
};

// 根据卦象编号获取卦象
export const getHexagramByNumber = (number: number): HexagramData | undefined => {
  return hexagramsData.find(hexagram => hexagram.number === number);
};

// 根据卦象名称获取卦象
export const getHexagramByName = (name: string): HexagramData | undefined => {
  return hexagramsData.find(hexagram => hexagram.name === name || hexagram.gua === name);
};

// 获取卦象解释（根据问题类型）
export const getHexagramInterpretation = (hexagram: HexagramData, questionType: string): string => {
  if (!hexagram) return '';
  
  switch (questionType) {
    case 'lost':
      return hexagram.situations.lost;
    case 'love':
      return hexagram.situations.love;
    case 'career':
      return hexagram.situations.career;
    case 'health':
      return hexagram.situations.health;
    case 'wealth':
      return hexagram.situations.wealth;
    case 'travel':
      return hexagram.situations.travel;
    case 'decision':
      return hexagram.situations.decision;
    case 'future':
      return hexagram.situations.future;
    default:
      return hexagram.situations.general;
  }
};

export default hexagramsData;